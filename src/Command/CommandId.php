<?php


namespace App\Command;


class CommandId
{
    private string $id;

    /**
     * CommandId constructor.
     * @param string $id
     */
    public function __construct(string $id)
    {
        $this->id = $id;
    }

    /**
     * @return string
     */
    public function getId(): string
    {
        return $this->id;
    }
}